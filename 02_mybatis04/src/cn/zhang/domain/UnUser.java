package cn.zhang.domain;

public class UnUser {
	private String id;
	private String name;
	private String pwd;
	private int age;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPwd() {
		return pwd;
	}
	public void setPwd(String pwd) {
		this.pwd = pwd;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	@Override
	public String toString() {
		return "UnUser [id=" + id + ", name=" + name + ", pwd=" + pwd + ", age=" + age + "]";
	}
	
}
